package com.skappz.dynamiclayouts.resources.customSpinner;

public class CustomSpinnerValues {

    private String id;
    private String value;

    public CustomSpinnerValues(String Id, String Value ){

        id = Id;
        value = Value;

    }


    public String getId() {
        return id;
    }

    public String getValue() {
        return value;
    }
}
